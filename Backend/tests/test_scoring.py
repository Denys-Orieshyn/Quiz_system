# ==============================================================================
# БІЗНЕС-ЛОГІКА: Функція обчислення підсумкового результату тестування
# ==============================================================================
def calculate_score(correct_count: int, total_count: int) -> float:
    """
    Математичний розрахунок відсотка успішності студента.
    Приймає кількість правильно відповідених питань та їх загальну кількість.
    Повертає округлений до 1 знака після коми відсоток (float).
    """
    # Захист від критичної помилки архітектури (ZeroDivisionError).
    # Якщо у тесті з якихось причин немає питань, система стійко повертає 0.0%
    if total_count == 0:
        return 0.0

    # Класична формула відсоткового співвідношення.
    # Метод round(..., 1) гарантує точність відображення (наприклад, 33.3% замість 33.3333333333)
    return round((correct_count / total_count) * 100, 1)


# ==============================================================================
# МОДУЛЬНІ ТЕСТИ (ВЕРИФІКАЦІЯ БІЗНЕС-ПРАВИЛ ЗА ДОПОМОГОЮ PYTEST)
# ==============================================================================

def test_score_all_correct():
    """
    Сценарій №1: Ідеальне проходження тесту (Позитивний тест-кейс).
    Перевіряє ситуацію, коли студент відповів правильно на всі питання.
    Очікуваний результат: рівно 100.0% успішності.
    """
    assert calculate_score(3, 3) == 100.0


def test_score_partial():
    """
    Сценарій №2: Часткова успішність із періодичним дробом (Граничний тест-кейс).
    Імітує ситуацію, коли з 3 питань правильно виконано лише 1.
    Математично 1/3 — це 33.3333...%
    Тест доводить, що механізм округлення працює коректно і повертає точну мітку 33.3%.
    """
    assert calculate_score(1, 3) == 33.3


def test_score_none_answered():
    """
    Сценарій №3: Повністю неправильне проходження (Негативний тест-кейс).
    Студент дав неправильні відповіді на всі питання або взагалі не обрав жодного варіанту.
    Очікуваний результат: рівно 0.0% успішності.
    """
    assert calculate_score(0, 3) == 0.0


def test_score_empty_test():
    """
    Сценарій №4: Обробка виняткової ситуації / порожній тест (Стрес-тест архітектури).
    Імітує крайній випадок, коли викладач опублікував тест, що не містить питань (0 з 0).
    Тест верифікує роботу блоку захисту `if total_count == 0`.
    Він доводить, що система не "впаде" з системною помилкою ділення на нуль,
    а коректно й безпечно поверне 0.0%.
    """
    assert calculate_score(0, 0) == 0.0